macro_rules! english_numerals {
    ($($n:ident : $v:expr),* $(,)?) => {
        #[allow(unused)]
        pub struct ItalianNumeral<T> {
            $( pub $n : $crate::Value<Self, T, $v> ),*
        }

        impl<T> $crate::Octet for ItalianNumeral<T> {
            const VALUES: Self = Self {
                $( $n : $crate::Value {
                    _o: std::marker::PhantomData,
                    _t: std::marker::PhantomData
                } ),*
            };
        }
    };
}

